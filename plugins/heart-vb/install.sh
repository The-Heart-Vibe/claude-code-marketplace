#!/usr/bin/env bash
# heart-vb plugin installer for The Heart Vibe marketplace.
# Idempotent — bezpiecznie odpalać wielokrotnie.
# Installs council CLI dependencies (uv, gemini-cli, codex auth check).
# Other skills (self-improving, vb-*, heart-custom) są markdown — auto-loaded by Claude Code.

set -euo pipefail

VENV="${COUNCIL_VENV:-$HOME/tools/council-env}"
WRAPPER_DIR="${COUNCIL_WRAPPER_DIR:-$HOME/.local/bin}"
WRAPPER="$WRAPPER_DIR/council"

say() { printf '\033[1;34m▸\033[0m %s\n' "$*"; }
warn() { printf '\033[1;33m⚠\033[0m %s\n' "$*"; }
ok() { printf '\033[1;32m✓\033[0m %s\n' "$*"; }
fail() { printf '\033[1;31m✗\033[0m %s\n' "$*" >&2; exit 1; }

# ── 1. Zależności systemowe ──────────────────────────────────────────────────
say "Sprawdzam zależności systemowe..."

if ! command -v uv >/dev/null 2>&1; then
  say "uv nie znaleziony — instaluję..."
  curl -LsSf https://astral.sh/uv/install.sh | sh
  export PATH="$HOME/.local/bin:$PATH"
  command -v uv >/dev/null 2>&1 || fail "Instalacja uv nieudana. Doinstaluj ręcznie: https://docs.astral.sh/uv/"
fi
ok "uv: $(uv --version)"

if ! command -v node >/dev/null 2>&1; then
  fail "Node.js wymagany dla Gemini CLI. Zainstaluj nodejs (brew install node lub nvm)."
fi
ok "node: $(node --version)"

if ! command -v gemini >/dev/null 2>&1; then
  say "Gemini CLI nie znaleziony — instaluję @google/gemini-cli globalnie..."
  npm install -g @google/gemini-cli
fi
ok "gemini: $(gemini --version 2>/dev/null || echo 'zainstalowany')"

# ── 2. Python venv + pakiet ──────────────────────────────────────────────────
say "Tworzę venv w $VENV..."
if [ ! -d "$VENV" ]; then
  uv venv "$VENV"
fi
say "Instaluję the-llm-council[gemini]..."
uv pip install --upgrade 'the-llm-council[gemini]>=0.7.16' --python "$VENV/bin/python" --quiet
ok "the-llm-council zainstalowany w $VENV"

# ── 3. Wrapper CLI ───────────────────────────────────────────────────────────
say "Tworzę wrapper $WRAPPER..."
mkdir -p "$WRAPPER_DIR"
cat > "$WRAPPER" <<EOF
#!/bin/bash
# Wrapper auto-generated by council plugin install.sh
VENV="\${COUNCIL_VENV:-$VENV}"
if [ ! -x "\$VENV/bin/council" ]; then
  echo "ERROR: council not found at \$VENV/bin/council" >&2
  echo "Re-run installer: bash <(curl -s https://raw.githubusercontent.com/The-Heart-Vibe/claude-code-marketplace/main/plugins/heart-vb/install.sh)" >&2
  exit 1
fi
exec "\$VENV/bin/council" "\$@"
EOF
chmod +x "$WRAPPER"
ok "wrapper: $WRAPPER"

# ── 4. PATH check ────────────────────────────────────────────────────────────
if ! echo "$PATH" | tr ':' '\n' | grep -qx "$WRAPPER_DIR"; then
  warn "$WRAPPER_DIR nie jest w \$PATH"
  warn "Dodaj do ~/.zshrc lub ~/.bashrc:"
  warn '  export PATH="$HOME/.local/bin:$PATH"'
  warn "Albo używaj pełnej ścieżki: $WRAPPER"
fi

# ── 5. Config template ───────────────────────────────────────────────────────
CONFIG_DIR="$HOME/.config/llm-council"
CONFIG_FILE="$CONFIG_DIR/config.yaml"
if [ ! -f "$CONFIG_FILE" ]; then
  say "Tworzę config $CONFIG_FILE..."
  mkdir -p "$CONFIG_DIR"
  cp "$(dirname "$0")/config/llm-council.example.yaml" "$CONFIG_FILE"
  ok "Config utworzony — edytuj $CONFIG_FILE jeśli chcesz custom defaults"
else
  ok "Config już istnieje: $CONFIG_FILE (zachowany)"
fi

# ── 6. Doctor ────────────────────────────────────────────────────────────────
say "Uruchamiam council doctor..."
echo ""
"$WRAPPER" doctor || true
echo ""

# ── 7. Hooks (opt-in) ────────────────────────────────────────────────────────
HOOK_VB_SRC="$(dirname "$0")/hooks/vb-suggest.sh"
HOOK_DEVTOOLS_SRC="$(dirname "$0")/hooks/devtools-suggest.sh"
HOOK_COWORK_SRC="$(dirname "$0")/hooks/cowork-suggest.sh"
HOOK_ROUTE_SRC="$(dirname "$0")/hooks/model-route.sh"
HOOK_VB_DST="$HOME/.claude/hooks/council-vb-suggest.sh"
HOOK_DEVTOOLS_DST="$HOME/.claude/hooks/heart-devtools-suggest.sh"
HOOK_COWORK_DST="$HOME/.claude/hooks/heart-cowork-suggest.sh"
HOOK_ROUTE_DST="$HOME/.claude/hooks/heart-model-route.sh"
SETTINGS="$HOME/.claude/settings.json"

if [ -f "$HOOK_VB_SRC" ] || [ -f "$HOOK_DEVTOOLS_SRC" ] || [ -f "$HOOK_COWORK_SRC" ] || [ -f "$HOOK_ROUTE_SRC" ]; then
  echo ""
  say "Opcjonalne hooki (rekomendowane: wszystkie 4)"
  cat <<EOF

   1. Venture Builder Suggest (vb-suggest.sh):
      Wykrywa zadania VB (decision/research/modeling/writing/validation/screening)
      i sugeruje właściwy skill (council, deep-research, financial-analyst, board-prep, ...).

   2. Research Tool Router (devtools-suggest.sh):
      Wykrywa URL-e + complex page signals → kieruje na chrome-devtools-mcp
      zamiast WebFetch (token-saving).

   3. Cowork Spawn Router (cowork-suggest.sh):
      Wykrywa multi-entity work (5 konkurentów, 3 scenariusze, sekcje IC memo)
      → sugeruje spawn N parallel cowork agents zamiast sequential w main.

   4. Model Router (model-route.sh):
      Klasyfikuje complexity tier → suggest model (Haiku trivial / Opus strategic /
      Sonnet routine default). Reminder że workers cowork = sonnet, orchestrator = opus.

   Opt-out per-prompt:
     "BEZ COUNCIL: ..."   — skip vb-suggest
     "BEZ DEVTOOLS: ..."  — skip devtools-suggest
     "BEZ COWORK: ..."    — skip cowork-suggest
     "BEZ ROUTE: ..."     — skip model-route

   Disable globalnie: usuń wpisy z $SETTINGS hooks.UserPromptSubmit
EOF

  if [ "${COUNCIL_INSTALL_HOOK:-ask}" = "yes" ]; then
    REPLY="y"
  elif [ "${COUNCIL_INSTALL_HOOK:-ask}" = "no" ]; then
    REPLY="n"
  else
    printf "\nZainstalować wszystkie 4 hooki? [y/N]: "
    read -r REPLY < /dev/tty || REPLY="n"
  fi

  case "$REPLY" in
    [Yy]*)
      mkdir -p "$(dirname "$HOOK_VB_DST")"
      [ -f "$HOOK_VB_SRC" ] && cp "$HOOK_VB_SRC" "$HOOK_VB_DST" && chmod +x "$HOOK_VB_DST"
      [ -f "$HOOK_DEVTOOLS_SRC" ] && cp "$HOOK_DEVTOOLS_SRC" "$HOOK_DEVTOOLS_DST" && chmod +x "$HOOK_DEVTOOLS_DST"
      [ -f "$HOOK_COWORK_SRC" ] && cp "$HOOK_COWORK_SRC" "$HOOK_COWORK_DST" && chmod +x "$HOOK_COWORK_DST"
      [ -f "$HOOK_ROUTE_SRC" ] && cp "$HOOK_ROUTE_SRC" "$HOOK_ROUTE_DST" && chmod +x "$HOOK_ROUTE_DST"

      # Backup settings.json
      if [ -f "$SETTINGS" ]; then
        cp "$SETTINGS" "${SETTINGS}.bak.$(date +%Y%m%d-%H%M%S)"
      fi

      python3 <<PYEOF
import json, os
settings_path = os.path.expanduser("~/.claude/settings.json")
hooks_to_install = [
    ("$HOOK_VB_DST", "council-vb-suggest.sh"),
    ("$HOOK_DEVTOOLS_DST", "heart-devtools-suggest.sh"),
    ("$HOOK_COWORK_DST", "heart-cowork-suggest.sh"),
    ("$HOOK_ROUTE_DST", "heart-model-route.sh"),
]

try:
    with open(settings_path) as f:
        cfg = json.load(f)
except (FileNotFoundError, json.JSONDecodeError):
    cfg = {}

cfg.setdefault("hooks", {})
cfg["hooks"].setdefault("UserPromptSubmit", [])

installed_count = 0
for hook_path, marker in hooks_to_install:
    if not os.path.exists(hook_path):
        continue
    # Idempotent — sprawdź czy już jest
    already = any(
        any(h.get("command", "").endswith(marker) for h in entry.get("hooks", []))
        for entry in cfg["hooks"]["UserPromptSubmit"]
    )
    if not already:
        cfg["hooks"]["UserPromptSubmit"].append({
            "matcher": ".*",
            "hooks": [{
                "type": "command",
                "command": hook_path,
                "timeout": 5
            }]
        })
        installed_count += 1

os.makedirs(os.path.dirname(settings_path), exist_ok=True)
with open(settings_path, "w") as f:
    json.dump(cfg, f, indent=2)

if installed_count == 0:
    print("✓ Hooks juz byly w settings.json")
else:
    print(f"✓ Zarejestrowano {installed_count} hook(s) w settings.json")
PYEOF
      ok "Hooks aktywne"
      ;;
    *)
      say "Hook pominięty. Aktywujesz później przez:"
      say "  bash <(curl -s https://raw.githubusercontent.com/The-Heart-Vibe/claude-code-marketplace/main/plugins/heart-vb/install.sh) z COUNCIL_INSTALL_HOOK=yes"
      ;;
  esac
fi

# ── 8. Codex CLI hint (opcjonalny, dla full Pattern F) ──────────────────────
echo ""
if ! command -v codex >/dev/null 2>&1; then
  say "Codex CLI — opcjonalny dla full Pattern F multi-LLM debate (3 LLMs)"
  cat <<EOF

   Bez Codex Pattern F działa jako 2-voice (Claude + Gemini) — w 90% przypadków wystarczy.
   Pełny 3-voice (Claude + Gemini + GPT-5) wymaga ChatGPT Plus + Codex CLI:

   1. Wymagana subskrypcja ChatGPT Plus (€22/mo): https://chatgpt.com
   2. Install Codex CLI (zewnętrzne narzędzie OpenAI — patrz dokumentacja Codex)
   3. Login: \`codex login\` (browser OAuth z ChatGPT)
   4. Verify: \`~/.local/bin/council doctor\` → codex powinien być OK

   Plugin DZIAŁA bez Codex — analityk dostaje Pattern F 2-voice fallback automatycznie.
EOF
else
  ok "Codex CLI wykryty: $(codex --version 2>/dev/null | head -1 || echo 'available')"
  if ! ~/.local/bin/council doctor 2>&1 | grep -q "codex.*OK"; then
    warn "Codex CLI zainstalowany ALE nie zalogowany — login: \`codex login\` (ChatGPT Plus required)"
    warn "Bez login: Pattern F będzie pomijać codex worker (graceful degradation = 2-voice)"
  fi
fi

# ── 9. Chrome DevTools MCP hint (wymagany dla devtools-suggest hook) ────────
echo ""
DEVTOOLS_INSTALLED="no"
if command -v claude >/dev/null 2>&1; then
  if claude mcp list 2>/dev/null | grep -qi "chrome-devtools"; then
    DEVTOOLS_INSTALLED="yes"
  fi
fi
# Fallback: check ~/.claude.json or ~/.claude/mcp.json for chrome-devtools entry
if [ "$DEVTOOLS_INSTALLED" = "no" ]; then
  for cfg in "$HOME/.claude.json" "$HOME/.claude/mcp.json" "$HOME/.config/claude/mcp.json"; do
    if [ -f "$cfg" ] && grep -qi "chrome-devtools" "$cfg" 2>/dev/null; then
      DEVTOOLS_INSTALLED="yes"
      break
    fi
  done
fi

if [ "$DEVTOOLS_INSTALLED" = "yes" ]; then
  ok "chrome-devtools-mcp wykryty — devtools-suggest hook ma czego potrzebuje"
else
  say "Chrome DevTools MCP — wymagany dla pełnego efektu devtools-suggest hook"
  cat <<EOF

   Hook \`devtools-suggest.sh\` sugeruje Claude użycie \`chrome-devtools-mcp\` zamiast
   WebFetch dla JS-heavy stron (G2, Crunchbase, LinkedIn) — token-efficient browsing.
   Bez tego MCP hook fire'uje ale Claude i tak spadnie do WebFetch.

   Instalacja (jedna z opcji):

   A) Jako MCP server (rekomendowane, działa natychmiast):
      claude mcp add chrome-devtools npx chrome-devtools-mcp@latest

   B) Jako Claude Code plugin (jeśli masz marketplace z chrome-devtools-mcp):
      /plugin install chrome-devtools-mcp

   Wymagania: Node.js (już zainstalowany ✓), Chrome/Chromium na maszynie.
   Plugin DZIAŁA bez tego — hook po prostu fallbackuje na WebFetch automatycznie.
EOF
fi

# ── 10. Next steps ───────────────────────────────────────────────────────────
cat <<EOF

──────────────────────────────────────────────────────────────────
✓ Council zainstalowany!
──────────────────────────────────────────────────────────────────

Auth dla providerów (sprawdź wyżej w doctor):

  claude     →  Powinien być OK (Claude Code już zalogowany)
                ❌ NIE DZIAŁA z poziomu aktywnej Claude Code session
  codex      →  Jeśli FAIL: \`codex login\` (wymaga ChatGPT Plus/Pro). OPCJONALNE — Pattern F bez tego = 2-voice fallback.
  gemini-cli →  Jeśli FAIL: \`gemini\` (OAuth przez Google Workspace)

Następnie w Claude Code:
  /plugin install heart-vb@the-heart-vibe   # jeśli nie zrobiłeś jeszcze
  /council <twoje pytanie>

Lub bezpośrednio z terminala:
  council run planner --mode assess "Twoja decyzja" \\
    --providers gemini-cli,codex --json

Jeśli włączyłeś hook — przy okazji każdej decyzji-podobnej wiadomości
Claude zapyta "uruchomić przez /council?". Możesz opt-out per-wiadomość:
  BEZ COUNCIL: jak działa SSE?
EOF
